pub fn slogan() -> String {
  let content = r#"
      		....                 ....
                 -+++++=:       .-=+++++
                  =+++++++:    =+++++++-
                   -++++==+-  ++==++++:
                     :=++=-+:===+++=:
                        .++:+=-+.
                            +:
                            =-
                         .-=++-:.
                       -++++-==++=:
                      =++ ._||. :+++-
                     :+++  |__]  +++:
                     -+++ _|__]  +++-
                      ++=   ||  -+++-
                       =++==:=++++:
                        .:=++++-:
                            :.
  "#;
  content.to_string()
}

pub fn banner(version: &str) -> String {
  let content: String = format!(
    r#"
             ___             _  ___ _____ _
            / __| ___ ___ __| |/ __|_   _| |
            \__ \/ -_) -_) _` | (__  | | | |__
            |___/\___\___\__,_|\___| |_| |____| version: {version}
  "#
  );
  content
}
